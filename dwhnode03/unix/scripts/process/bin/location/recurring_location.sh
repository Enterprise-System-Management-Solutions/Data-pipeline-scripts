#!/bin/bash


PATH=$PATH:$HOME/.local/bin:$HOME/bin

export PATH

export TMP=/tmp
export TMPDIR=$TMP

export ORACLE_HOSTNAME=dwhnode03
export ORACLE_UNQNAME=dwhdb03
export ORACLE_BASE=/data01/app/oracle
export ORACLE_HOME=$ORACLE_BASE/product/19.0.0/dbhome_1
export ORA_INVENTORY=/data01/app/oraInventory
export ORACLE_SID=dwhdb03
export DATA_DIR=/data01/oradata

export PATH=/usr/sbin:/usr/local/bin:$PATH
export PATH=$ORACLE_HOME/bin:$PATH

export LD_LIBRARY_PATH=$ORACLE_HOME/lib:/lib:/usr/lib
export CLASSPATH=$ORACLE_HOME/jlib:$ORACLE_HOME/rdbms/jlib

sqlplus  -s <<EOF
dwh_user03/dwh_user_123
SET echo off
SET head off
SET feedback off
set sqlblanklines on
REM "WHENEVER SQLERROR EXIT SQL.SQLCODE"

TRUNCATE TABLE L3_RECURRING DROP STORAGE;
INSERT INTO  L3_RECURRING 
SELECT LPAD(R375_CHARGINGPARTYNUMBER,13,880) AS R375_CHARGINGPARTYNUMBER, R373_MAINOFFERINGID, R385_OFFERINGID, R41_DEBIT_AMOUNT/1000000 AS R41_DEBIT_AMOUNT, R377_CYCLEBEGINTIME
FROM L1_RECURRING_TEMP_FULL@DWH03TODWH01;
COMMIT;

INSERT INTO RECURRING_LOCATION_FCT
SELECT DATE_KEY, R375_CHARGINGPARTYNUMBER, CALLINGCELLID, R373_MAINOFFERINGID, R385_OFFERINGID, R41_DEBIT_AMOUNT,R377_CYCLEBEGINTIME
FROM
(
SELECT DATE_KEY, R375_CHARGINGPARTYNUMBER, CALLINGCELLID, R373_MAINOFFERINGID, R385_OFFERINGID, R41_DEBIT_AMOUNT,R377_CYCLEBEGINTIME,DF, ROW_NUMBER() OVER(PARTITION BY R375_CHARGINGPARTYNUMBER ORDER BY DF ASC ) CHARGINGTIME
FROM
(SELECT DATE_KEY, R375_CHARGINGPARTYNUMBER, CALLINGCELLID, R373_MAINOFFERINGID, R385_OFFERINGID, R41_DEBIT_AMOUNT,R377_CYCLEBEGINTIME,CYCLEBEGINTIME1+ANSWERTIMESTAMP1 AS DF
FROM
    (
    SELECT DATE_KEY, R375_CHARGINGPARTYNUMBER, CALLINGCELLID, R373_MAINOFFERINGID, R385_OFFERINGID, R41_DEBIT_AMOUNT,R377_CYCLEBEGINTIME, CYCLEBEGINTIME,ANSWERTIMESTAMP,
    CASE
    WHEN CYCLEBEGINTIME>0
    THEN CYCLEBEGINTIME
    WHEN CYCLEBEGINTIME<0
    THEN 0
    END CYCLEBEGINTIME1,

    CASE
    WHEN ANSWERTIMESTAMP>0
    THEN ANSWERTIMESTAMP
    WHEN ANSWERTIMESTAMP<0
    THEN 0
    END ANSWERTIMESTAMP1

        FROM
        (
        SELECT /*+ PARALLEL (A,16)*/ DATE_KEY, R375_CHARGINGPARTYNUMBER, CALLINGCELLID, R373_MAINOFFERINGID, R385_OFFERINGID, R41_DEBIT_AMOUNT,R377_CYCLEBEGINTIME, CYCLEBEGINTIME,ANSWERTIMESTAMP
        FROM
        (SELECT /*+ PARALLEL (A,16)*/ (SELECT DATE_KEY FROM DATE_DIM WHERE TO_CHAR(DATE_VALUE,'RRRRMMDD')=TO_CHAR(SYSDATE-1,'RRRRMMDD'))DATE_KEY, 
                                        R375_CHARGINGPARTYNUMBER, M09_LOCATION AS CALLINGCELLID, R373_MAINOFFERINGID, R385_OFFERINGID, R41_DEBIT_AMOUNT,R377_CYCLEBEGINTIME, R377_CYCLEBEGINTIME-M07_ANSWERTIMESTAMP AS CYCLEBEGINTIME,M07_ANSWERTIMESTAMP-R377_CYCLEBEGINTIME AS ANSWERTIMESTAMP 
        FROM L3_RECURRING B
        LEFT OUTER JOIN  L1_MSC A
        ON R375_CHARGINGPARTYNUMBER=M04_MSISDNAPARTY
        AND PROCESSED_DATE BETWEEN TO_DATE(SYSDATE-4,'DD/MM/RRRR') AND TO_DATE(SYSDATE-1,'DD/MM/RRRR')
        AND SUBSTR(R377_CYCLEBEGINTIME,1,8)=SUBSTR(M07_ANSWERTIMESTAMP,1,8)
        )
        )
    )
    )
)
WHERE CHARGINGTIME=1;
COMMIT;
EXIT
EOF
