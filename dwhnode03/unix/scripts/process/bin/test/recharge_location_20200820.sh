PATH=$PATH:$HOME/.local/bin:$HOME/bin

export PATH

export TMP=/tmp
export TMPDIR=$TMP

export ORACLE_HOSTNAME=dwhnode03
export ORACLE_UNQNAME=dwhdb03
export ORACLE_BASE=/data01/app/oracle
export ORACLE_HOME=$ORACLE_BASE/product/19.0.0/dbhome_1
export ORA_INVENTORY=/data01/app/oraInventory
export ORACLE_SID=dwhdb03
export DATA_DIR=/data01/oradata

export PATH=/usr/sbin:/usr/local/bin:$PATH
export PATH=$ORACLE_HOME/bin:$PATH

export LD_LIBRARY_PATH=$ORACLE_HOME/lib:/lib:/usr/lib
export CLASSPATH=$ORACLE_HOME/jlib:$ORACLE_HOME/rdbms/jlib

sqlplus  -s <<EOF
dwh_user03/dwh_user_123
SET echo off
SET head off
SET feedback off
REM "WHENEVER SQLERROR EXIT SQL.SQLCODE"

TRUNCATE TABLE L3_RECHARGE DROP STORAGE;
INSERT INTO L3_RECHARGE
SELECT /*+PARALLEL(A,16)*/ RE3_RECHARGE_AMT/1000000, LPAD(RE7_THIRD_PARTY_NUMBER,13,880), RE6_PRI_IDENTITY, RE24_RESULT_CODE,TO_CHAR(TO_DATE( SUBSTR(RE30_ENTRY_DATE,1,14), 'YYYYMMDDHH24MISS')+6/24, 'YYYYMMDDHH24MISS') RE30_ENTRY_DATE, RE489_MAINOFFERINGID
FROM L1_RECHARGE_TEMP_FULL@DWH03TODWH01 A;
COMMIT;

INSERT INTO RECHARGE_LOCATION_FCT( DATE_KEY,RE6_PRI_IDENTITY, CALLINGCELLID, RE3_RECHARGE_AMT, RE7_THIRD_PARTY_NUMBER, RE24_RESULT_CODE, RE30_ENTRY_DATE, RE489_MAINOFFERINGID)
SELECT /*+ PARALLEL (A,16)*/ DATE_KEY,RE6_PRI_IDENTITY, M09_LOCATION, RE3_RECHARGE_AMT, RE7_THIRD_PARTY_NUMBER, RE24_RESULT_CODE, RE30_ENTRY_DATE, RE489_MAINOFFERINGID
FROM
(SELECT /*+ PARALLEL (A,16)*/ (SELECT DATE_KEY FROM DATE_DIM WHERE TO_CHAR(DATE_VALUE,'RRRRMMDD')=TO_CHAR(SYSDATE-1,'RRRRMMDD'))DATE_KEY,RE6_PRI_IDENTITY, M09_LOCATION, RE3_RECHARGE_AMT, RE7_THIRD_PARTY_NUMBER, RE24_RESULT_CODE, RE30_ENTRY_DATE, RE489_MAINOFFERINGID
FROM L3_RECHARGE B
LEFT OUTER JOIN  L1_MSC A
ON RE6_PRI_IDENTITY=M04_MSISDNAPARTY
AND SUBSTR(RE30_ENTRY_DATE,1,12)=SUBSTR(M07_ANSWERTIMESTAMP,1,12)
AND TRUNC(PROCESSED_DATE)=TRUNC(SYSDATE-1)
AND M05_MSISDNBPARTY='74656C65636861726765'
)A
GROUP BY DATE_KEY,RE6_PRI_IDENTITY, M09_LOCATION, RE3_RECHARGE_AMT, RE7_THIRD_PARTY_NUMBER, RE24_RESULT_CODE, RE30_ENTRY_DATE, RE489_MAINOFFERINGID
;
COMMIT;
EXIT
EOF