#!/bin/bash


PATH=$PATH:$HOME/.local/bin:$HOME/bin

export PATH

export TMP=/tmp
export TMPDIR=$TMP

export ORACLE_HOSTNAME=dwhnode03
export ORACLE_UNQNAME=dwhdb03
export ORACLE_BASE=/data01/app/oracle
export ORACLE_HOME=$ORACLE_BASE/product/19.0.0/dbhome_1
export ORA_INVENTORY=/data01/app/oraInventory
export ORACLE_SID=dwhdb03
export DATA_DIR=/data01/oradata

export PATH=/usr/sbin:/usr/local/bin:$PATH
export PATH=$ORACLE_HOME/bin:$PATH

export LD_LIBRARY_PATH=$ORACLE_HOME/lib:/lib:/usr/lib
export CLASSPATH=$ORACLE_HOME/jlib:$ORACLE_HOME/rdbms/jlib

sqlplus  -s <<EOF
dwh_user03/dwh_user_123
SET echo on
SET head off
SET feedback off
set sqlblanklines on
WHENEVER SQLERROR EXIT SQL.SQLCODE
INSERT INTO L1_MSC( PROCESSED_DATE, FILE_NAME, M01_CALLTYPE, M02_IMSI, M03_IMEI, M04_MSISDNAPARTY, M05_MSISDNBPARTY, M07_ANSWERTIMESTAMP, M08_CALLDUR, M09_LOCATION, M10_LAST_LOCATION, M11_CAUSEOFTERMINATION)
SELECT PROCESSED_DATE,FILE_NAME, RECORD_NAME, MN12_IMSI,IMEI, MN01_SERVEDMSISDN, MN02_CALLINGNUMBER, MN03_ANSWERTIME, CALLDURATION, LOCATION,  M09_LAST_LOCATION,PREFERRED
FROM
(SELECT PROCESSED_DATE,FILE_NAME, RECORD_NAME, MN12_IMSI,MN11_IMEI, MN01_SERVEDMSISDN, MN02_CALLINGNUMBER,MN03_ANSWERTIME, CALLDURATION, MN09_LOCATION , M09_LAST_LOCATION,MN13_CAUSEFORTERM,TERMINATION_NAME,
CASE
WHEN MN13_CAUSEFORTERM =0
THEN TERMINATION_NAME
WHEN MN13_CAUSEFORTERM !=0
THEN NULL

END PREFERRED,

CASE
WHEN LENGTH(MN11_IMEI)>13
THEN MN11_IMEI
WHEN LENGTH(MN11_IMEI)<14
THEN NULL

END IMEI,

CASE
WHEN LENGTH(MN09_LOCATION)=15
THEN MN09_LOCATION
WHEN LENGTH(MN09_LOCATION)!=15
THEN NULL

END LOCATION

FROM
(SELECT /*+ parallel(A,16)*/ PROCESSED_DATE,FILE_NAME, RECORD_NAME, MN12_IMSI,MN11_IMEI, LPAD(MN01_SERVEDMSISDN,13,880) AS MN01_SERVEDMSISDN, MN02_CALLINGNUMBER, MN03_ANSWERTIME,MN04_CALLDURATION/10000 AS CALLDURATION, LPAD(MN07_FSTLAC||MN08_FSTCI,15,47004) AS MN09_LOCATION ,LPAD(MN09_LASTLAC||MN10_LASTCI,15,47004) AS M09_LAST_LOCATION,MN13_CAUSEFORTERM,TERMINATION_NAME
FROM L1_MSC_NOKIA_old partition($1) A, RECORD_TYPE_DIM B, TERMINATION_DIM C
WHERE MN05_RECORDTYPE=RECORD_ID
AND MN11_IMEI IS NOT NULL
AND TERMINATION_ID=MN13_CAUSEFORTERM(+)
and REGEXP_LIKE(MN04_CALLDURATION, '^[[:digit:]]+$')
)
)
group by PROCESSED_DATE,FILE_NAME, RECORD_NAME, MN12_IMSI,IMEI, MN01_SERVEDMSISDN, MN02_CALLINGNUMBER,MN03_ANSWERTIME, CALLDURATION, LOCATION,  M09_LAST_LOCATION,PREFERRED
;
commit;
EXIT
EOF

