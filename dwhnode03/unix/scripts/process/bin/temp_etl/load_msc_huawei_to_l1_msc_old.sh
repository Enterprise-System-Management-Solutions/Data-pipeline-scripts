#!/bin/bash


PATH=$PATH:$HOME/.local/bin:$HOME/bin

export PATH

export TMP=/tmp
export TMPDIR=$TMP

export ORACLE_HOSTNAME=dwhnode03
export ORACLE_UNQNAME=dwhdb03
export ORACLE_BASE=/data01/app/oracle
export ORACLE_HOME=$ORACLE_BASE/product/19.0.0/dbhome_1
export ORA_INVENTORY=/data01/app/oraInventory
export ORACLE_SID=dwhdb03
export DATA_DIR=/data01/oradata

export PATH=/usr/sbin:/usr/local/bin:$PATH
export PATH=$ORACLE_HOME/bin:$PATH

export LD_LIBRARY_PATH=$ORACLE_HOME/lib:/lib:/usr/lib
export CLASSPATH=$ORACLE_HOME/jlib:$ORACLE_HOME/rdbms/jlib

sqlplus  -s <<EOF
dwh_user03/dwh_user_123
SET echo on
SET head off
SET feedback off
set sqlblanklines on
WHENEVER SQLERROR EXIT SQL.SQLCODE
INSERT INTO L1_MSC (PROCESSED_DATE, FILE_NAME, M01_CALLTYPE, M02_IMSI, M03_IMEI, M04_MSISDNAPARTY, M05_MSISDNBPARTY, M06_FORWARDEDMSISDN, M07_ANSWERTIMESTAMP, M08_CALLDUR, M09_LOCATION, M10_LAST_LOCATION, M11_CAUSEOFTERMINATION)
SELECT PROCESSED_DATE, FILE_NAME, MH01_CALLTYPE, MH02_SERVEDIMSI, IMEI, MH04_APARTYMSISDN, MH05_BPARTYMSISDN, MH06_FORWARDINGMSISDN, MH07_ORIGINATIONTIME, MH08_CALLDURATION, LOCATION, MH11_LAST_LOCATION, MH09_CAUSEFORTERM
FROM
(SELECT PROCESSED_DATE, FILE_NAME, MH01_CALLTYPE, MH02_SERVEDIMSI, MH03_SERVEDIMEI, MH04_APARTYMSISDN, MH05_BPARTYMSISDN, MH06_FORWARDINGMSISDN, MH07_ORIGINATIONTIME, MH08_CALLDURATION, MH10_GLOBAAREAID, MH11_LAST_LOCATION, MH09_CAUSEFORTERM,
CASE
WHEN LENGTH(MH03_SERVEDIMEI)>13
THEN MH03_SERVEDIMEI
WHEN LENGTH(MH03_SERVEDIMEI)<14
THEN NULL

END IMEI,

CASE
WHEN LENGTH(MH10_GLOBAAREAID)=15
THEN MH10_GLOBAAREAID
WHEN LENGTH(MH10_GLOBAAREAID)!=15
THEN NULL

END LOCATION

FROM
(SELECT /*+ PARALLEL (A,16) */ TO_DATE(TO_CHAR(SYSDATE-1, 'MM/DD/YYYY'), 'MM/DD/YYYY') AS PROCESSED_DATE, FILE_NAME, MH01_CALLTYPE, MH02_SERVEDIMSI, MH03_SERVEDIMEI, LPAD(MH04_APARTYMSISDN,13,880) as MH04_APARTYMSISDN, MH05_BPARTYMSISDN, MH06_FORWARDINGMSISDN, '20'||MH07_ORIGINATIONTIME as MH07_ORIGINATIONTIME, MH08_CALLDURATION, MH10_GLOBAAREAID, MH11_LAST_LOCATION, MH09_CAUSEFORTERM
FROM L1_MSC_HUAWEI A
WHERE MH04_APARTYMSISDN IS NOT NULL
AND MH07_ORIGINATIONTIME IS NOT NULL
AND MH07_ORIGINATIONTIME IS NOT NULL
AND LENGTH(MH08_CALLDURATION)<5 )
)
GROUP BY PROCESSED_DATE, FILE_NAME, MH01_CALLTYPE, MH02_SERVEDIMSI, IMEI, MH04_APARTYMSISDN, MH05_BPARTYMSISDN, MH06_FORWARDINGMSISDN, MH07_ORIGINATIONTIME, MH08_CALLDURATION, LOCATION, MH11_LAST_LOCATION, MH09_CAUSEFORTERM
;
COMMIT;
EXIT
EOF

